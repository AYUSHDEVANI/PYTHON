# range(start index,end index,increament)

for n in range(5):
    print(n)

print()

for i in range(1,6):
    print(i)

print()

for i in range(1,6,2):
    print(i)

print()

# Table print 
for i in range(1,11):
    print("2  X ",i," = ",2*i)

print()

# reverse loop
for i in range(10,0,-1):
    print(i)
print()

for i in range(10,0,-1):
    print("2  X ",i," = ",2*i)